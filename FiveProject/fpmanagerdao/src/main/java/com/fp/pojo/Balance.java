package com.fp.pojo;

public class Balance {
    private Long bid;

    private Double overmoney;

    private Double taxrate;

    private Long quickmoney;

    private Long mid;

    public Long getBid() {
        return bid;
    }

    public void setBid(Long bid) {
        this.bid = bid;
    }

    public Double getOvermoney() {
        return overmoney;
    }

    public void setOvermoney(Double overmoney) {
        this.overmoney = overmoney;
    }

    public Double getTaxrate() {
        return taxrate;
    }

    public void setTaxrate(Double taxrate) {
        this.taxrate = taxrate;
    }

    public Long getQuickmoney() {
        return quickmoney;
    }

    public void setQuickmoney(Long quickmoney) {
        this.quickmoney = quickmoney;
    }

    public Long getMid() {
        return mid;
    }

    public void setMid(Long mid) {
        this.mid = mid;
    }
}