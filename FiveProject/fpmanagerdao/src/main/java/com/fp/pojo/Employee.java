package com.fp.pojo;

import java.util.Date;

public class Employee {
    private Double employeeId;

    private String employeeName;

    private String employeeGender;

    private Date employeeBirth;

    private String employeeIdCard;

    private String employeeNativePlace;

    private String employeeDepartment;

    private Double employeeTel;

    private String employeePost;

    private Date employeeEntrytime;

    private Double employeeWage;

    private String employeeRemark;

    public Double getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(Double employeeId) {
        this.employeeId = employeeId;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeeGender() {
        return employeeGender;
    }

    public void setEmployeeGender(String employeeGender) {
        this.employeeGender = employeeGender;
    }

    public Date getEmployeeBirth() {
        return employeeBirth;
    }

    public void setEmployeeBirth(Date employeeBirth) {
        this.employeeBirth = employeeBirth;
    }

    public String getEmployeeIdCard() {
        return employeeIdCard;
    }

    public void setEmployeeIdCard(String employeeIdCard) {
        this.employeeIdCard = employeeIdCard;
    }

    public String getEmployeeNativePlace() {
        return employeeNativePlace;
    }

    public void setEmployeeNativePlace(String employeeNativePlace) {
        this.employeeNativePlace = employeeNativePlace;
    }

    public String getEmployeeDepartment() {
        return employeeDepartment;
    }

    public void setEmployeeDepartment(String employeeDepartment) {
        this.employeeDepartment = employeeDepartment;
    }

    public Double getEmployeeTel() {
        return employeeTel;
    }

    public void setEmployeeTel(Double employeeTel) {
        this.employeeTel = employeeTel;
    }

    public String getEmployeePost() {
        return employeePost;
    }

    public void setEmployeePost(String employeePost) {
        this.employeePost = employeePost;
    }

    public Date getEmployeeEntrytime() {
        return employeeEntrytime;
    }

    public void setEmployeeEntrytime(Date employeeEntrytime) {
        this.employeeEntrytime = employeeEntrytime;
    }

    public Double getEmployeeWage() {
        return employeeWage;
    }

    public void setEmployeeWage(Double employeeWage) {
        this.employeeWage = employeeWage;
    }

    public String getEmployeeRemark() {
        return employeeRemark;
    }

    public void setEmployeeRemark(String employeeRemark) {
        this.employeeRemark = employeeRemark;
    }
}