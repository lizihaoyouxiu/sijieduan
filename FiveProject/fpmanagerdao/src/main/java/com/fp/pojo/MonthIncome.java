package com.fp.pojo;

public class MonthIncome {
    private Long mid;

    private Double monthnoney;

    private Double allwages;

    private Double allmoney;

    private String month;

    private Long bid;

    public Long getMid() {
        return mid;
    }

    public void setMid(Long mid) {
        this.mid = mid;
    }

    public Double getMonthnoney() {
        return monthnoney;
    }

    public void setMonthnoney(Double monthnoney) {
        this.monthnoney = monthnoney;
    }

    public Double getAllwages() {
        return allwages;
    }

    public void setAllwages(Double allwages) {
        this.allwages = allwages;
    }

    public Double getAllmoney() {
        return allmoney;
    }

    public void setAllmoney(Double allmoney) {
        this.allmoney = allmoney;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public Long getBid() {
        return bid;
    }

    public void setBid(Long bid) {
        this.bid = bid;
    }
}